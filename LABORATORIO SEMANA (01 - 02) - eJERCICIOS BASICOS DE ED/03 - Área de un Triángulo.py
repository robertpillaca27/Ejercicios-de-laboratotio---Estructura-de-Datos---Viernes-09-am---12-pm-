"""Área de un Triángulo:
3) Pide la base y la altura de un triángulo al usuario y calcula su área."""
    
# Solicitar al usuario que ingrese la base del triángulo
base = int(input("Ingrese la base del triángulo: "))
# Solicitar al usuario que ingrese la altura del triángulo
altura = int(input("Ingrese la altura del triángulo: "))

# Calcular el área del triángulo utilizando la fórmula (base * altura) / 2
áreaTriangulo = base * altura / 2

# Imprimir el resultado del área del triángulo
print("El área del triángulo es igual a:", int(áreaTriangulo))

